/** Interpolation values for a `{placeholder}` in a catalog string. */
export type TVars = Record<string, string | number>;

/**
 * Pure lookup: the string for `key` in `lang`, falling back to sq, then the key.
 * When `vars` is given, `{name}` placeholders are substituted (e.g. "+{n} XP").
 */
export function translate(key: string, lang: Lang, vars?: TVars): string {
  const e = STRINGS[key];
  const raw = e ? (e[lang] || e.sq || key) : key;
  if (!vars) return raw;
  return raw.replace(/\{(\w+)\}/g, (_m, name: string) => (name in vars ? String(vars[name]) : `{${name}}`));
}

const STORAGE_KEY = 'murlan.lang.v1';
function loadLang(): Lang {
  try { return localStorage.getItem(STORAGE_KEY) === 'en' ? 'en' : 'sq'; } catch { return 'sq'; }
}
if (typeof document !== 'undefined') document.documentElement.lang = loadLang();

interface LangStore {
  lang: Lang;
  setLang: (l: Lang) => void;
}
export const useLangStore = create<LangStore>((set) => ({
  lang: loadLang(),
  setLang: (lang) => {
    try { localStorage.setItem(STORAGE_KEY, lang); } catch { /* ignore */ }
    if (typeof document !== 'undefined') document.documentElement.lang = lang;
    set({ lang });
  },
}));

/** Reactive translator bound to the current language (supports `{var}` interpolation). */
export function useT(): (key: string, vars?: TVars) => string {
  const lang = useLangStore((s) => s.lang);
  return (key: string, vars?: TVars) => translate(key, lang, vars);
}
